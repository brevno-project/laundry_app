const interpolate = (text: string, vars?: Record<string, string | number>) => {
  if (!vars) return text;
  return text.replace(/\{\{(\w+)\}\}/g, (_, key) => String(vars[key] ?? ""));
};

const getInitialLanguage = (): UiLanguage => {
  return "ru";
};

const getInitialTheme = (): UiTheme => {
  return "light";
};

export const UiProvider = ({ children }: { children: React.ReactNode }) => {
  const [language, setLanguage] = useState<UiLanguage>(getInitialLanguage);
  const [theme, setTheme] = useState<UiTheme>(getInitialTheme);
  const [themeHydrated, setThemeHydrated] = useState(false);

  useEffect(() => {
    if (typeof window === "undefined") return;
    const storedLanguage = localStorage.getItem("appLanguage") as UiLanguage | null;
    if (storedLanguage === "ru" || storedLanguage === "en" || storedLanguage === "ko" || storedLanguage === "ky") {
      setLanguage(storedLanguage);
    }
  }, []);

  useLayoutEffect(() => {
    if (typeof window === "undefined") return;
    const storedTheme = localStorage.getItem("appTheme") as UiTheme | null;
    if (storedTheme === "light" || storedTheme === "dark") {
      setTheme(storedTheme);
    }
    setThemeHydrated(true);
  }, []);

  useEffect(() => {
    if (typeof window === "undefined") return;
    localStorage.setItem("appLanguage", language);
    document.documentElement.lang = language;
  }, [language]);

  useLayoutEffect(() => {
    if (typeof window === "undefined") return;
    const html = document.documentElement;
    if (theme === "dark") {
      html.classList.add("dark");
    } else {
      html.classList.remove("dark");
    }
    if (themeHydrated) {
      localStorage.setItem("appTheme", theme);
    }
  }, [theme, themeHydrated]);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const dict = translations[language] || translations.ru;
      const base = dict[key] || translations.ru[key] || key;
      return interpolate(base, vars);
    },
    [language]
  );

  const value = useMemo(
    () => ({
      language,
      setLanguage,
      theme,
      setTheme,
      t,
    }),
    [language, theme, t]
  );

  return <UiContext.Provider value={value}>{children}</UiContext.Provider>;
};

export const useUi = () => {
  const context = useContext(UiContext);
  if (!context) {
    throw new Error("useUi must be used within UiProvider");
  }
  return context;
};

